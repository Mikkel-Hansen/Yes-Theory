function showProfile() { //Creates function called showProfile
            let element = document.getElementById("profile-container"); //Create variable called element from the HTML ID "profile-container"
            element.classList.toggle("vis-profile-container"); //Toggle on/off the the class "vis-profile-container" for the above variable
        }

